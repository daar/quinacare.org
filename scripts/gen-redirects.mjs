import fs from "fs";
import path from "path";

const ROOT = process.cwd();
const ROUTES = {
  news: { nl: "actueel", en: "news", es: "noticias" },
  donate: { nl: "doneer", en: "donate", es: "donar" },
  projects: { nl: "projecten", en: "projects", es: "proyectos" },
  fundraisers: { nl: "acties", en: "fundraisers", es: "campañas" },
  search: { nl: "zoeken", en: "search", es: "buscar" },
  "putumayo-loop": {
    nl: "putumayo-loop",
    en: "putumayo-run",
    es: "putumayo-carrera",
  },
};
const prefix = (lang) => (lang === "nl" ? "" : `/${lang}`);
const R = {};

// A. Fixed routes: index + child routes for changed segments. Child redirect
// destinations must match a real route with the same param name.
const CHILDREN = {
  news: ["[slug]"],
  donate: ["return"],
  projects: ["[slug]"],
  fundraisers: ["[slug]"],
  search: [],
  "putumayo-loop": ["[year]"],
};
for (const [canon, map] of Object.entries(ROUTES)) {
  for (const lang of ["nl", "en", "es"]) {
    if (map[lang] === canon) continue; // unchanged for this lang
    const p = prefix(lang);
    R[`${p}/${canon}`] = `${p}/${map[lang]}`;
    for (const child of CHILDREN[canon] || []) {
      R[`${p}/${canon}/${child}`] = `${p}/${map[lang]}/${child}`;
    }
  }
}

// B. Content-page slug changes (old shared slug -> native)
const pageChanges = {
  nl: {
    about: "over-ons",
    organization: "organisatie",
    hospital: "ziekenhuis",
    "become-partner": "word-partner",
    "donate/sponsor-booklet": "doneer/sponsor-booklet",
  },
  en: {
    "word-vrijwilliger": "become-volunteer",
    jaarverslagen: "annual-reports",
  },
  es: {
    about: "sobre-nosotros",
    organization: "organizacion",
    "become-partner": "hazte-socio",
    "word-vrijwilliger": "hazte-voluntario",
    jaarverslagen: "informes-anuales",
    privacy: "privacidad",
    contact: "contacto",
    "donate/sponsor-booklet": "donar/sponsor-booklet",
  },
};
for (const [lang, m] of Object.entries(pageChanges)) {
  const p = prefix(lang);
  for (const [oldS, newS] of Object.entries(m))
    R[`${p}/${oldS}`] = `${p}/${newS}`;
}

// C. Generated post slug changes (EN/ES): old /news/<key> -> new <native route>/<slug>
for (const lang of ["en", "es"]) {
  const base = path.join(ROOT, "src/content/news", lang);
  const walk = (dir) => {
    for (const e of fs.readdirSync(dir, { withFileTypes: true })) {
      const fp = path.join(dir, e.name);
      if (e.isDirectory()) walk(fp);
      else if (e.name.endsWith(".mdoc")) {
        const t = fs.readFileSync(fp, "utf8");
        const slug = t.match(/^slug:\s*"([^"]+)"/m)?.[1];
        const key = t.match(/^translationKey:\s*"([^"]+)"/m)?.[1];
        if (slug && key && slug !== key) {
          R[`${prefix(lang)}/news/${key}`] =
            `${prefix(lang)}/${ROUTES.news[lang]}/${slug}`;
        }
      }
    }
  };
  walk(base);
}

// D. Moved download PDFs
Object.assign(R, {
  "/downloads/sponsor-booklet/251110_Sponsorboekje.pdf":
    "/nl/sponsorboekje/251110_Sponsorboekje.pdf",
  "/downloads/sponsor-booklet/251125_Sponsor-booklet.pdf":
    "/en/sponsor-booklet/251125_Sponsor-booklet.pdf",
  "/downloads/sponsor-booklet/251125_Cuaderno-de-patrocinio.pdf":
    "/es/cuaderno-de-patrocinio/251125_Cuaderno-de-patrocinio.pdf",
  "/downloads/Quina-Care-Introductieboekje-Vrijwilligers.pdf":
    "/nl/vrijwilligersboekje/Quina-Care-Introductieboekje-Vrijwilligers.pdf",
});

const out =
  "// AUTO-GENERATED by scripts/gen-redirects (route localization). Edit the\n" +
  "// generator, not this file. Old URLs -> new native URLs.\n" +
  "export default " +
  JSON.stringify(R, null, 2) +
  ";\n";
fs.writeFileSync(path.join(ROOT, "src/data/routeRedirects.mjs"), out);
console.log("Wrote", Object.keys(R).length, "redirects");
